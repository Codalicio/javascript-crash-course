// Intro to Variables :

// Variables are used to store information/data that can be used and modified later anytime.
// Variable names are case sensitive.

// Declaring a Variable :
var firstName = "Amit";

// Using a variable :
console.log(firstName);

// Changing value of the variable :
firstName = "Sonu";
console.log(firstName);
